# ------------------------- Commands For Creating virtual Environment -----------------------

# 1.   Open Powershell as admin
# 2.   Change path where you want to install virtual environment using cd command like cd Document
# 3.   Install package virtualenv using command pip install virtualenv
# 4.1. Create VirtualEnvironment by using command virtualenv <Environment-Name>
# 4.2. If want to copy environment of system then use command virtualenv --system-site-packages <Environment-Name>
# 6.   Activate Environment By using Command .\<Environment-Name>\Script\activate
# 7.   After Doing Everything deactivate Virtual Environment by using Command deactivate

